// and operator
let firstName = "raju"
let age = 21

if(firstName[0] === "r" && age> 18){
    console.log("your name starts with r and you are 18+"); 
}else{
    console.log("something wrong");   
}

// or operator
let firstName2 = "raju"
let age2 = 21

if(firstName2[0] === "r" || age2> 18){
    console.log("one or both are corrected"); 
}else{
    console.log("both are incorrect");   
}